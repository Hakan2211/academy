// Extracts the single <Quiz> from each lesson MDX into a static practice-item
// bank (src/content/practice/bank.generated.ts). The MDX <Quiz> stays the single
// source of truth; re-run this whenever quizzes change:
//
//   node scripts/extract-practice-items.mjs
//
// UTF-8 safe by construction (Node fs read/write utf8) — never edit the bank by
// hand, and never run this through PowerShell Get-Content (mojibake trap).

import { readFileSync, writeFileSync, readdirSync, mkdirSync, statSync } from 'node:fs'
import { join, dirname, basename } from 'node:path'

const ROOT = process.cwd()
const LESSONS_ROOT = join(ROOT, 'src/content/lessons')
const OUT = join(ROOT, 'src/content/practice/bank.generated.ts')

function parseFrontmatter(src) {
  const m = src.match(/^---\r?\n([\s\S]*?)\r?\n---/)
  const fm = {}
  if (!m) return fm
  for (const line of m[1].split(/\r?\n/)) {
    const i = line.indexOf(':')
    if (i <= 0) continue
    const k = line.slice(0, i).trim()
    let v = line.slice(i + 1).trim().replace(/^["']|["']$/g, '')
    fm[k] = v
  }
  return fm
}

// Parse the <Quiz ... /> attributes. Handles "string" values and {js expression}
// values (arrays / numbers, inline or multi-line, trailing commas, UTF-8 chars).
function extractQuiz(src) {
  const start = src.indexOf('<Quiz')
  if (start < 0) return null
  const end = src.indexOf('/>', start)
  if (end < 0) return null
  const block = src.slice(start + '<Quiz'.length, end)

  const attrs = {}
  let i = 0
  const n = block.length
  const skipWs = () => {
    while (i < n && /\s/.test(block[i])) i++
  }

  while (true) {
    skipWs()
    if (i >= n) break
    const name = /^[A-Za-z0-9_]+/.exec(block.slice(i))?.[0]
    if (!name) break
    i += name.length
    skipWs()
    if (block[i] !== '=') break
    i++
    skipWs()

    const ch = block[i]
    if (ch === '"' || ch === "'") {
      i++
      let val = ''
      while (i < n && block[i] !== ch) {
        if (block[i] === '\\' && i + 1 < n) {
          val += block[i + 1]
          i += 2
          continue
        }
        val += block[i]
        i++
      }
      i++ // closing quote
      attrs[name] = val
    } else if (ch === '{') {
      let depth = 0
      let expr = ''
      while (i < n) {
        const c = block[i]
        if (c === '{') {
          depth++
          if (depth === 1) {
            i++
            continue
          }
        } else if (c === '}') {
          depth--
          if (depth === 0) {
            i++
            break
          }
        }
        expr += c
        i++
      }
      // Trusted, our own content. Evaluates JS array/number literals.
      attrs[name] = Function(`"use strict"; return (${expr});`)()
    } else {
      break
    }
  }
  return attrs
}

// Every subject folder under src/content/lessons/<subject>/*.mdx
const subjects = readdirSync(LESSONS_ROOT).filter((d) =>
  statSync(join(LESSONS_ROOT, d)).isDirectory(),
)

const items = []
const failures = []

for (const subject of subjects) {
  const dir = join(LESSONS_ROOT, subject)
  const files = readdirSync(dir)
    .filter((f) => f.endsWith('.mdx'))
    .sort()
  for (const f of files) {
    const src = readFileSync(join(dir, f), 'utf8')
    const fm = parseFrontmatter(src)
    const contentSlug = fm.contentSlug || `${subject}/${basename(f, '.mdx')}`
    const quiz = extractQuiz(src)
    if (
      !quiz ||
      typeof quiz.prompt !== 'string' ||
      !Array.isArray(quiz.options) ||
      typeof quiz.correctIndex !== 'number'
    ) {
      failures.push(`${subject}/${f}`)
      continue
    }
    items.push({
      id: contentSlug,
      contentSlug,
      subject: contentSlug.split('/')[0],
      lessonTitle: fm.title || '',
      prompt: quiz.prompt,
      options: quiz.options,
      correctIndex: quiz.correctIndex,
      explanation: typeof quiz.explanation === 'string' ? quiz.explanation : '',
    })
  }
}

items.sort((a, b) => a.contentSlug.localeCompare(b.contentSlug))

const out = `// AUTO-GENERATED by scripts/extract-practice-items.mjs — do not edit by hand.
// Source of truth: the <Quiz> in each lesson MDX. Re-run after editing quizzes:
//   node scripts/extract-practice-items.mjs
import type { PracticeItem } from '#/lib/practice'

export const PRACTICE_BANK: Array<PracticeItem> = ${JSON.stringify(items, null, 2)}
`

mkdirSync(dirname(OUT), { recursive: true })
writeFileSync(OUT, out, 'utf8')

console.log(`Extracted ${items.length} practice items -> ${OUT}`)
if (failures.length) {
  console.warn(`\n⚠️  Failed to parse ${failures.length} file(s):`)
  for (const f of failures) console.warn('   ' + f)
} else {
  console.log('All lessons parsed cleanly. ✅')
}
